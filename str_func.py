def cap_letters(value):
    """Функция принимает на вход строку
    и возвращает ее со всеми заглавными
    буквами"""
    return value.upper()


def title_letters(value):
    """функцию, которая делает заглавными
    первые буквы каждого слова в строке,
    поступившей на вход"""
    return value.title()
